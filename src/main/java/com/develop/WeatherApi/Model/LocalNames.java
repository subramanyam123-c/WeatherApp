package com.develop.WeatherApi.Model;

// Class to represent the local names for a city
public class LocalNames {
    private String en;
    private String ru;

    // Getters and setters
    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getRu() {
        return ru;
    }

    public void setRu(String ru) {
        this.ru = ru;
    }
}
